package com.sucre.function;

public class CheckIn extends Login{

	public CheckIn() {
		super();
	}

	public CheckIn(String id, String pass) {
		super(id, pass);
	}

	public CheckIn(String inputdata) {
		super(inputdata);
	}

	@Override
	public String getId() {
		return super.getId();
	}

	@Override
	public void setId(String id) {
		super.setId(id);
	}

	@Override
	public String getPass() {
		return super.getPass();
	}

	@Override
	public void setPass(String pass) {
		super.setPass(pass);
	}

	@Override
	public String getCookie() {
		return super.getCookie();
	}

	@Override
	public void setCookie(String cookie) {
		super.setCookie(cookie);
	}

	@Override
	public String getUid() {
		return super.getUid();
	}

	@Override
	public void setUid(String uid) {
		super.setUid(uid);
	}

	@Override
	public String getS() {
		return super.getS();
	}

	@Override
	public void setS(String s) {
		super.setS(s);
	}

	@Override
	public String toString() {
		return super.toString();
	}

	
	
	

}
